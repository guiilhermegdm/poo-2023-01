package com.github.guiilhermegdm.poo.t08;

public class Ponto {

    public int xAxis;
    public int yAxis;

    public Ponto(int xAxis, int yAxis) {
        this.xAxis = xAxis;
        this.yAxis = yAxis;
    }

    public int getxAxis() {
        return xAxis;
    }

    public int getyAxis() {
        return yAxis;
    }
}
